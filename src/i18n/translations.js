const TRANSLATIONS = {
  en: {},
  fr: FR_TRANSLATIONS,
};

const interpolate = (message, replacements) => Object.entries(replacements).reduce(
  (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
  message
);

export const normalizeLanguage = (language, fallback = 'en') => {
  const normalizedLanguage = String(language ?? '').trim().toLowerCase().slice(0, 2);
  return SUPPORTED_LANGUAGES.includes(normalizedLanguage)
    ? normalizedLanguage
    : normalizeLanguage(fallback, 'en');
};

export const getTranslation = (language, message, replacements = {}) => {
  const normalizedLanguage = normalizeLanguage(language);
  const source = String(message ?? '');
  const translated = TRANSLATIONS[normalizedLanguage]?.[source] ?? source;
  return interpolate(translated, replacements);
};
